# Tipo int e float
# int - numerop inmtieros
# O tipo int representa qualquer numero
# positivo ou negativo. int sem sinal é considerado
# positivo.
print(11) # int
print(-11) # int
print(0)

# float -  número com ponto flutuante
# o tipo float representa qualquer numero
# positivo ou negativo com ponto flutuante
# float sem sinal é considerado psositivo
print(1.1, 10.11)
print(0.0, -1.5)

